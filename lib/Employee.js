class Employee {
    constructor(name, id, email, role) {
        this.name = this.name;
        this.id = this.id;
        this.email = this.email;
        this.role = this.role;
};

    getName() {
        return this.name;
    };

    getID() {
        return this.id;
    };

    getEmail() {
        return this.email;
    };

    getRole() {
        return this.role;
    };
};

module.exports = Employee;